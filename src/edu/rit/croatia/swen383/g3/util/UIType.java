package edu.rit.croatia.swen383.g3.util;

/**
 * Enum representing the available user interface types in the system.
 */
public enum UIType {
    /** Text-based UI type */
    TEXT,
    /** Swing-based UI type */
    SWING,
    /** JavaFX-based UI type */
    JAVAFX,
    /** UI type for displaying sensor statistics */
    STAT,
    /** UI type for displaying sensor forecasts */
    FORECAST;
}
